#used when the number of iterations is limited
for i in range(10):
    print(i)
    #prints 0 to 9
for i in range(10):
    print(i+1)
    #prints 0 to 10
for i in range(50,100):
    print(i)
    #prints 50 to 99
for i in range (50,100,2):
    print(i)
    #prints even numbers from 50 to 98
